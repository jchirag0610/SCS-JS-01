// take input using prompt
let full_name = prompt("enter your full name"); 

// display input value using alert
alert(`Welcome ${full_name}`); 

// split input value using space
console.log(full_name.split(' ')); 

// using template literal and check if input contains a string using includes
console.log(`${full_name} contains 'chirag': ${full_name.includes('chirag')}`); 

//replace a string with another one using replace
console.log(full_name.replace("chirag","shiny")); 

// pick some characters from a string using slice.
console.log(full_name.slice(0,4)); 





